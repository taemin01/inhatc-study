var a;
a = 10;
document.write("a = "+ a+ "<br />");
document.write("a++ 실행 결과 = "+ (a++)+ "<br />");
document.write("a++ 실행 후 a = "+ a+ "<br />");
document.write("++a 실행 결과 = "+ (++a)+ "<br />");
document.write("a-- 실행 결과 = "+ (a--)+ "<br />");
document.write("a-- 실행 후 a = "+ a+ "<br />");
document.write("--a 실행 결과 = "+ (--a));